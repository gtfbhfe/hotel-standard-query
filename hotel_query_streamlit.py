st.markdown("依据《中央和国家机关工作人员赴地方差旅住宿费标准明细表》及四川省2026年1月新规")

city_input = st.text_input("请输入城市名称", placeholder="例如：南京、成都市、北京")

if city_input:
    matched_city, info = get_city_standard(city_input.strip())
    if info is None:
        st.error(f"未找到“{city_input}”的住宿标准，请检查城市名或查看数据来源。")
    else:
        # 显示所属省份（使用省份映射字典）
        province = CITY_PROVINCE.get(matched_city, "未知省份")
        st.markdown(f"**所属地区**：{province} · {matched_city}")
        
        st.subheader(f"【{matched_city}】住宿标准")
        basic = info['basic']
        if basic:
            col1, col2, col3 = st.columns(3)
            col1.metric("部级", f"{basic[0]} 元/天")
            col2.metric("司局级", f"{basic[1]} 元/天")
            col3.metric("其他人员", f"{basic[2]} 元/天")
        if info['peak']:
            st.markdown("#### 旺季浮动标准")
            for period, a, b, c in info['peak']:
                st.markdown(f"- **{period}**：部级 {a}元，司局级 {b}元，其他人员 {c}元")

st.markdown("---")
st.markdown("📄 **数据来源**：[财政部《中央和国家机关差旅费管理办法》](http://www.mof.gov.cn/gkml/caizhengwengao/201405/t20140520_1085793.htm) 及 [四川省2026年1月新规](https://sc.people.com.cn/n2/2026/0105/c345167-41463447.html)")
